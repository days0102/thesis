      const svg = select(svgRef.value).attr("height", height);

      // Define our scales
      const xScale = scaleLinear()
        .domain([0, max(data.map((d) => d.value))])
        .range([0, dimensions.width.value]);

      const yScale = scaleBand()
        .domain(data.map((d) => d.key))
        .range([0, height])
        .padding(0.1);

      // Define and draw our axis
      const xAxis = axisBottom(xScale).ticks(2);
      svg
        .select(".x-axis")
        .style("transform", `translateY(${height}px)`)
        .call(xAxis);

      const yAxis = axisLeft(yScale);
      svg.select(".y-axis").call(yAxis);

      // Draw the rect to their proper position
      svg
        .selectAll("rect")
        .data(data)
        .join("rect")
        .attr("x", (d) => xScale(0))
        .attr("y", (d) => yScale(d.key))
        .attr("width", (d) => xScale(d.value))
        .attr("height", yScale.bandwidth())
        .attr("fill", (d) => {
          if (userColorScale != null) {
            if (d.key === "other") {
              return "#DEDEDE";
            } else {
              return userColorScale(d.key);
            }
          }
        })
        .on("mouseenter", (d) => {
          svg
            .selectAll(".tooltip")
            .data([d])
            .join("text")
            .attr("class", "tooltip")
            .attr("x", xScale(d.value) / 2)
            .attr("y", yScale(d.key) + yScale.bandwidth() / 2)
            .text(d.value)
            .attr("text-anchor", "middle")
            .transition()
            .attr("opacity", 1);
        })
        .on("mouseleave", () => svg.select(".tooltip").remove())
        .transition();
    });
    return { divRef, svgRef };
  },
});
</script>

